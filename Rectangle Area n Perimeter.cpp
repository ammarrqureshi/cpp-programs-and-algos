#include <iostream>
using namespace std;
int main()
{
	//// Program Area and Perimeter of Rectangle
	float width;
	float length;
	cout<<"Enter the Width of Rectangle:";
	cin>>width;
	cout<<endl;
	cout<<"Enter the Length of Rectangle: ";
	cin>>length;
	cout<<endl;
	cout<<"Area of Rectangle: ";
	cout<<width*length<<endl<<endl;
	cout<<"Perimeter of Rectangle: ";
	cout<<2*(length+width);

}
